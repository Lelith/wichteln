<h2>Allgemein</h2>
<h3>Werde ich ein Geschenk bekommen?</h3>
Das kann dir keiner sagen. Vielleicht. Vielleicht nicht. Das ist der Reiz am Wichteln. Auch wenn du jemandem einen Wunsch erf&uuml;llst, kannst du nicht davon ausgehen, selbst etwas zur&uuml;ck zu bekommen.

<h3>Es l&auml;uft etwas schief, ich habe eine Frage, wann soll ich mich beim Weihnachtswichtel melden?</h3>
So fr&uuml;h wie m&ouml;glich! Wenn du eine Ausnahme geregelt haben m&ouml;chtest, wenn du ein Problem mit dem Skript hast, wenn etwas nicht geklappt hat, wenn du &uuml;berpr&uuml;fen m&ouml;chstest, ob deine W&uuml;nsche oder Geschenkreservierungen trotzdem in der Datenbank angekommen sind, wenn du ein Geschenk ohne ID bekommen hast, wenn du die Daten deines Partners verschusselt hast und bei allen anderen Eventualit&auml;ten, ist der Weihnachtswichtel dein kompetenter Ansprechpartner. <a target="_blank" href="https://www.naehkromanten.net/forum/memberlist.php?mode=viewprofile&u=10714">Schick ihm eine PN</a>, dann k&uuml;mmert er sich um deinen Fall.

<h3>Ich m&ouml;chte meinen Nick &auml;ndern... soll ich das jetzt, vorher oder nachher machen?</h3>
Wenn du dich umbennenst, w&auml;hrend das Wichteln l&auml;uft, bringt uns das hier ganz sch&ouml;n durcheinander. Es w&auml;re also toll, wenn du das zeitlich abpasst.




<h2>Zu Versand- und Empfangsbest&auml;tigungen, Paketen und Versand</h2>

<h3>Was ist die Trackingnummer?</h3>
Je nach Anbieter hei&szlig;t es auch "Sendungsnummer". Mit dieser Nummer k&ouml;nnt ihr online nachverfolgen, wo sich eurer Paket befindet, ob es aufgeliefert wurde oder nicht. Das ist f&uuml;r uns auch der Nachweis, dass ihr tats&auml;chlich etwas verschickt habt. <br>
Bei der Deutschen Post/DHL kann man sein Geschenk als Paket (nicht P&auml;ckchen) oder Einschreiben aufgeben, dann erhaltet ihr automatisch eine Trackingnummer. Bei Hermes oder GLS gibt es Trackingnummern standadm&auml;&szlig;ig.<br>
Nochmal zur Info: Wer &uuml;ber Landesgrenzen hinweg verschickt, braucht keine Trackingnummer, sondern kann einfach das Stichwort INTERNATIONAL eintragen.

<h3>Kann ich mein Paket auch pers&ouml;nlich abliefern?</h3>
Das ist kein Problem. Gib in diesem Fall in der Versandbest&auml;titgung bitte das Stichwort PERS&Ouml;NLICH an.

<h3>Was mache ich, wenn mein Partner sein Geschenk nicht erh&auml;lt?</h3>
Wenn du in der Sendungsverfolgung per Trackingnummer siehst, dass das Geschenk nicht ausgeliefert werden konnte, auf Abholung wartet oder &auml;hnliches, dann kontaktiere den Empf&auml;nger bitte pers&ouml;nlich oder bitte den Weihnachtswichtel dies zu tun. Damit geht vielleicht die &Uuml;berraschung verloren, aber am Ende z&auml;hlt, dass die Geschenke auch ankommen.

<h3>Ich habe vergessen, die Geschenk-ID draufzuschreiben.</h3>
Bitte beichte dieses Vers&auml;umnis dem Weihnachtswichtel, so dass er Bescheid wei&szlig; wenn dein Partner ein Geschenk ohne ID erh&auml;lt.

<h3>Ich habe ein Geschenk ohne ID erhalten.</h3>
Dann melde dich auf jeden Fall beim Weihnachtswichtel! Er wird dir eine ID nennen, mit der du das Geschenk best&auml;tigen kannst.

<h3>Woher wei&szlig; ich, ob mein Paket angekommen ist?</h3>
Mit der Trackingnummer kannst du in der Regel online pr&uuml;fen, wo sich das Paket befindet. Wenn dein Partner den Empfang best&auml;tigt, erh&auml;ltst du au&szlig;erdem eine E-Mail und kannst sicher sein, dass alles geklappt hat.

<h3>Ich m&ouml;chte mein potentielles Geschenk an eine andere Adresse als meine Wohnadresse bekommen.</h3>
Dein Wichtel erh&auml;lt die Adresse, die du in den Kontaktdaten eintr&auml;gst und schickt das Paket dort hin. Trag dort also die gew&uuml;nschte Lieferadresse ein. Denk auch daran, m&ouml;gliche Adresszus&auml;tze ("Bei M&uuml;ller", "Firma XY" oder &auml;hnliches) anzugeben.




<h2>Zu den W&uuml;nschen</h2>

<h3>Wie kann ich sehen, ob meine W&uuml;nsche noch frei sind? Warum werden meine W&uuml;nsche nicht angezeigt, wenn ich danach suche?</h3>
Die eigenen W&uuml;nsche werden dir selbst nicht angezeigt. Ob du ein Geschenk erh&auml;ltst, soll doch eine &Uuml;berraschung bleiben!

<h3>Kann ich mehrere W&uuml;nsche erf&uuml;llen?</h3>
Nat&uuml;rlich. Wenn du Zeit und Lust hast, mehreren Leuten eine Freude zu machen, dann kannst du das gerne tun. Aber erst, wenn du den Versand des ersten Geschenks best&auml;tigt hast, kannst du ein weiteres ausw&auml;hlen.

<h3>Muss ich ein Geschenk basteln, wenn ich meine W&uuml;nsche hier ver&ouml;ffentliche?</h3>
Nein. Mit dem Eintragen deiner W&uuml;nsche verpflichtest du dich zu nichts.

<h3>Kann man auch zwei W&uuml;nsche erf&uuml;llt bekommen?</h3>
Nein. Jeder bekommt maximal einen seiner drei W&uuml;nsche erf&uuml;llt.


<h2>Zu den Geschenken</h2>

<h3>Was ist, wenn ich es nicht schaffe, das Geschenk fertig zu stellen?</h3>
Wenn etwas Gravierendes dazwischen kommt, dann melde dich m&ouml;glichst rechtzeitig(!) beim Weihnachtswichtel. Wenn die Wunschanfrage-Phase noch nicht abgeschlossen ist, kann der Wunsch vielleicht noch jemand anderem vermittelt werden.

<h3>Ich hab ein tolles Geschenk erhalten, soll ich gleich Fotos im Forum posten?</h3>
Im Interesse aller ist es fairer, mit Fotos zu warten, bis die Deadline vor&uuml;ber ist. Im Regelfall gibt es einen Fotothread, in dem jeder seine Geschenke vorzeigen kann.



<h2>Zum Zeitplan</h2>

<h3>Was passiert, wenn ich die Deadline nicht einhalte? Oder wenn ich mein Geschenk gar nicht verschicke?</h3>
Leute, die die Deadline verpasst haben, werden f&uuml;r das n&auml;chste Wichteln im kommenden Jahr gesperrt.<br>
Wer sein Geschenk gar nicht verschickt, wird auf Dauer f&uuml;r das Wichteln gesperrt.<br>
Im Zweifel ist es sinnvoller auf Weihnachtswichtel-PNs zu antworten  und den Kopf nicht in den Sand zu stecken. Wer ehrlich erkl&auml;rt, was passiert ist, hat gr&ouml;&szlig;ere Chancen auf eine Ausnahme als jemand, der tot spielt.



<h2>Zum B&uuml;rgen</h2>

<h3>Kann ich f&uuml;r mehrere User b&uuml;rgen?</h3>
Ja, daf&uuml;r gibt es keine Beschr&auml;nkung. Sei dir aber bitte der mehrfachen Verantwortung bewusst, die du dann tr&auml;gst.

<h3>Was passiert, wenn der, f&uuml;r den ich b&uuml;rge, seine Pflicht nicht erf&uuml;llt?</h3>
Wenn das Geschenk nicht oder nicht rechtzeitig abgeschickt wird, dann &uuml;bertr&auml;gt sich die Strafe daf&uuml;r auf dich.
