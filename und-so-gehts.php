<a name="wunsche"><h2>W&uuml;nschen</h2></a>
Ihr m&uuml;sst <b>genau drei</b> W&uuml;nsche angeben. Mit einem oder zwei kann man nicht teilnehmen. Ihr k&ouml;nnt eure W&uuml;nsche im Nachhinein nicht editieren, also gebt Acht, was ihr schreibt!<br>
<br>
W&uuml;nsche sollen Dinge sein, die man ohne Riesen-Aufwand selbst herstellen kann. M&ouml;glich ist alles, N&auml;hen, Filzen, Stricken oder etwas ganz anderes wie z.B. Seifen herstellen. Denkt aber daran, dass es <b>kleine Sachen</b> sein sollten, etwas, das man in relativ kurzer Zeit fertig kriegen kann, nichts was zu teuer oder zu aufw&auml;ndig ist.<br>
<br>
Wichtig: Lasst eurem potentiellen Wichtel Raum f&uuml;r eigene Ideen, seid aber nicht zu unspezifisch. Es ist immer gut, Optionen und Vorlieben anzugeben, aber nicht als Muss. "Ich w&uuml;nsche mir etwas aus Karostoff" macht es eurem Wichtel schwer, ein passendes Geschenk zu finden. "Ich w&uuml;nsche mir einen Rock wie diesen aus Shop xy" l&auml;sst keinen Spielraum f&uuml;r eigene Ideen. Versucht einen guten Mittelweg zu finden.<br>
Wenn ihr euch nicht sicher seid, ob ein Wunsch zu anspruchsvoll ist oder zu grob gefasst, dann nehmt in eure Liste noch einen bescheidenen oder genauer beschriebenen Wunsch auf, so dass der Wichtel eine Auswahl hat. Auch daf&uuml;r sind die drei W&uuml;nsche da, dass ihr Anf&auml;nger und Fortgeschrittene gleicherma&szlig;en ansprecht.<br>
<br>
Denkt auch daran, eure W&uuml;nsche ausreichend zu beschreiben. Wir k&ouml;nnen im Nachhinein keine Zusatzinfos an die Wichtel weiterleiten, also denkt bitte an die wichtigen Infos, Ma&szlig;e, Besonderheiten und Details, die zu nennen sind. Z.B. "Ich w&uuml;nsche mir eine Handytasche" - dann unbedingt die Abmessung des Handys angeben!<br>
<br>
Zus&auml;tzlich habt ihr noch ein Feld, in dem ihr allgemeine Besonderheiten, Vorlieben und &auml;hnliches notieren k&ouml;nnt, wie z.B. eure Lieblingsfarben oder Abneigungen. Allergien, soweit sie relevant sind, solltet ihr dort auf jeden Fall nennen!<br>
<br>
Wichtig: Alles was ihr zu euren W&uuml;nschen eintragt, soll <b>anonym</b> sein! Erst wenn jemand euren Wunsch ausw&auml;hlt, bekommt er die Information, zu wem der Wunsch geh&ouml;rt.<br>
<br>
Ebenfalls wichtig: In die Beschreibung geh&ouml;ren <b>keine Links</b>! Wenn ihr eure Vorstellungen nicht in ein paar S&auml;tzen zusammenfassen k&ouml;nnt, dann ist der Wunsch wahrscheinlich zu gro&szlig;. Denkt dran, dass es keine Auftragsarbeit ist, die ihr hier vergebt!
<br>
<br>
Hier eine Liste mit Dingen, die nicht in die Wunschbeschreibungen geh&ouml;ren:<br>
<ul>
<li>"Ich w&uuml;nsche mir ein Halstuch oder alternativ eine M&uuml;tze". Keine Alternativen. Das sind zwei W&uuml;nsche, die ihr separat angeben solltet.</li>
<li>Keine identischen W&uuml;nsche, die sich nur in der Ausf&uuml;hrung, Farbe oder Details unterscheiden.<br></li>
<li>"Ich w&uuml;nsche mir ein Spitzenschirm/Wollumhang." Die Geschenke werden per Post verschickt, also &uuml;berlegt, ob sich eure Idee auch daf&uuml;r eignet. Schwere und sperrige Gegenst&auml;nde sind teuer im Versand.</li>
<li>Es ist in Ordnung, auch W&uuml;nsche einzutragen, die sich gezielt an erfahrene Wichtel wenden, aber auch hier sollte der Aufwand im Rahmen bleiben und es sollte maximal ein derartiger Wunsch in den dreien vorkommen.</li>
</ul>
Alle W&uuml;nsche werden vom Weihnachtswichtel durchgelesen. Wenn er etwas f&uuml;r unangemessen h&auml;lt, wird er euch kontaktieren und euch den Wunsch zur &Uuml;berarbeitung zur&uuml;ckgeben.<br>
