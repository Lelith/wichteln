<a name="teilnahme"><h2>Beschr&auml;nkungen zur Teilnahme</h2></a>
Teilnehmen kann jeder registrierte User des Forums mit <b>mindestens 100 Posts</b>. Egal, ob als Teilnehmer, Wichtel oder beides, die Grenze sind immer 100 Posts. Wir machen die einzige Ausnahme von dieser Regel, wenn es einen <a href="#burgen">B&uuml;rgen</a> gibt.<br>
Wer im Forum im Suche&Biete oder Aktivit&auml;ten-Bereich gesperrt ist, kann auch nicht am Wichteln teilnehmen. Ebenso diejenigen, die letztes Jahr die Deadline nicht eingehalten haben.<br>
