<a name="ablauf"><h2>Ablauf</h2></a>
Ihr k&ouml;nnt &quot;Teilnehmer&quot; oder &quot;Wichtel&quot; sein, oder auch beides.
<br>
<i>Teilnehmer</i> denken sich drei W&uuml;nsche aus, drei Dinge, die sie gerne bekommen w&uuml;rden (genauere Angaben und Beschr&auml;nkungen <a href="#wunsche">siehe unten</a>). Drei deshalb, damit die Wichtel etwas Auwahl haben. Jeder bekommt trotzdem maximal einen Wunsch erf&uuml;llt! Diese W&uuml;nsche tragt ihr hier ein, so werden sie in die Datenbank geschrieben und den anderen zug&auml;nglich gemacht.<br>
<i>Wichtel</i> kann jeder sein, egal, ob er/sie selber W&uuml;nsche eingetragen hat oder nicht. Der Wichtel w&auml;hlt sich einen Wunsch eines Teilnehmers aus, den er/sie gerne erf&uuml;llen m&ouml;chte. Unter &quot;Beschenken&quot; k&ouml;nnt ihr W&uuml;nsche suchen und euch anzeigen lassen.<br>
<br>
Jeder Wichtel, der einen Wunsch ausgew&auml;hlt hat, bekommt die Daten seines Partners und hat entsprechend des <a href="#zeitplan">Zeitplans</a> Zeit, das Geschenk anzufertigen. Die Deadline zum Verschicken ist fest, also denkt daran, rechtzeitig fertig zu werden!<br>
Wenn ihr dann ein Geschenk verschickt oder erhalten habt, dann best&auml;tigt dies bitte auf jeden Fall hier im Skript. Nur so k&ouml;nnen wir &uuml;berpr&uuml;fen, wer seine Wichtelpflicht erf&uuml;llt hat.<br>
