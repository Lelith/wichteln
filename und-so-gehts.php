<a name="paket"><h2>Was in und auf das Paket soll</h2></a>
<li>Legt eine Karte dazu mit ein paar Gr&uuml;&szlig;en und eurem Nick/Namen, so wei&szlig; der Beschenkte gleich, von wem die &Uuml;berraschung kommt. Und ihr k&ouml;nnt die Geschenk-ID dort noch einmal aufschreiben.</li>
<li>Packt das Geschenk in Geschenkpapier ein, bevor ihr es in einen Karton oder Umschlag tut, so kann der Empf&auml;nger nach dem &Ouml;ffnen entscheiden, ob er mit dem Auspacken vielleicht noch bis Weihnachten warten m&ouml;chte.</li>
<li>Macht auf dem Paket von au&szlig;en klar, dass es sich um ein Wichtelgeschenk handelt. Leute die z.B. viel bei eBay kaufen, erkennen sonst nicht, was da angekommen ist und best&auml;tigen den Erhalt nicht.</li>
</ul>
