<a name="zeitplan"><h2>Zeitplan</h2></a>
<div align="center"><img src="zeitplan2015.jpg" border="0" alt="Zeitplan"></div><br>
Beginn des Wichteln ist am Montag, den 07.11.2016. Ab diesem Tag k&ouml;nnt ihr eure W&uuml;nsche im Skript eintragen. Es werden allerdings noch keine W&uuml;nsche vergeben, wir sammeln erst einmal. Wunschanfragen k&ouml;nnt ihr ab Sonntag, den 13.11.2016 stellen. Ab diesem Zeitpunkt werden W&uuml;nsche vergeben, ihr k&ouml;nnt die B&uuml;rge-Funktion nutzen und die Best&auml;tigungen f&uuml;r Versand und Empfang sind aktiv. Jetzt kann die Bastelphase starten! Ihr k&ouml;nnt auch eure W&uuml;nsche noch eine Woche lang eintragen, bis zum 20.11.2016. Dann ist Annahmeschlu&szlig;. Einen Wunsch heraussuchen k&ouml;nnt ihr noch bis zum 27.11.2016 (je nach Auslastung auch l&auml;nger). Dann solltet ihr langsam in die Puschen kommen, nach insgesamt 4 Wochen Bastelzeit ist Deadline zum Verschicken am Montag, den <b>12.12.2016</b>. Das ist defintiv! Danach k&ouml;nnt ihr nur noch den Versand und Empfang eines Paketes best&auml;tigen.<br>
Alle angegebenen Tage sind inklusive, wenn da also steht: "W&uuml;nsche eintragen bis 20.11.2016", dann k&ouml;nnt ihr den letzten Wunsch am 20.11.2016 um 23:59 Uhr eintragen.<br>
