<a name="verschicken"><h2>Verschicken</h2></a>
Es gibt eine Deadline f&uuml;r das Verschicken der Geschenke. Wer bis zu diesem Datum sein Paket nicht auf die Post gebracht hat, bekommt keine Gnadenfrist. Zwei wichtige Dinge sind beim Verschicken zu beachten: <br>
1. W&auml;hlt einen Versandanbieter, bei dem ihr eine <b>Trackingnummer</b> bekommt, mit der der Status der Lieferung verfolgt werden kann. Einzige Ausnahme ist der internationale Versand. &uuml;ber L&auml;ndergrenzen hinweg kann der versicherte Versand sehr teuer werden, gebt in diesem Fall statt einer Trackingnummer das Stichwort INTERNATIONAL an.<br>
2. Schreibt die <b>Geschenk-ID</b>, die ihr zusammen mit den Daten eures Partners erhalten habt, auf jeden Fall auf das Paket. Am sichersten ist es, die ID zur Adresse zu schreiben ("A. Wichtel [ID:12345], Waldweg 15, Wichtelhausen"), so wird sie nicht &uuml;berklebt und ist sofort sichtbar. Euer Partner braucht diese Nummer um den Erhalt des Geschenkes zu best&auml;tigen.<br>
<br>
Wenn ihr das Paket verschickt habt, dann <b>best&auml;tigt</b> dies auf jeden Fall hier im Skript! Wir pr&uuml;fen, wer zur Deadline nicht verschickt hat. Ihr erspart uns viel Arbeit, wenn wir nicht nachfragen m&uuml;ssen.<br>
Bei der Best&auml;tigung gebt ihr die Geschenk-ID und Trackingnummer an, das ist unbedingt n&ouml;tig! Nur so kann euer Partner ein eventuell verschollenes Paket ausfindig machen lassen.<br>
