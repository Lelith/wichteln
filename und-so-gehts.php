<a name="burgen"><h2>B&uuml;rgen</h2></a>
User mit weniger als 100 Posts k&ouml;nnen als Wichtel teilnehmen, wenn sie einen B&uuml;rgen haben. Sie k&ouml;nnen dann selbst keine W&uuml;nsche &auml;u&szlig;ern, aber sie k&ouml;nnen W&uuml;nsche von anderen erf&uuml;llen. Der B&uuml;rge ist dabei daf&uuml;r verantwortlich, dass der Wichtel sein Geschenk rechtzeitig verschickt und sich an die Regeln h&auml;lt. Wenn etwas schief geht, wird der B&uuml;rge zur Verantwortung gezogen, d.h. er/sie landet auf der Sperrliste. B&uuml;rgen m&uuml;ssen mindestens 500 Posts haben.<br>
B&uuml;rgen funktioniert so, dass der B&uuml;rge den Namen des Users angibt, f&uuml;r den er b&uuml;rgen m&ouml;chte. Dadurch wird dieser User freigeschaltet und kann am Wichteln teilnehmen.<br>
