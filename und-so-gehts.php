<a name="wunschanfragen"><h2>Wunschanfragen</h2></a>
Wenn ihr einen Wunsch erf&uuml;llen wollt, startet ihr eine Wunschanfrage. Ihr braucht keine W&uuml;nsche eingetragen haben, um anfragen zu k&ouml;nnen.<br>
Ihr habt die M&ouml;glichkeit, die W&uuml;nsche nach bestimmten Kriterien zu filtern und gezielt zu suchen oder euch alle noch nicht vergebenen W&uuml;nsche anzeigen zu lassen.<br>
<br>
Aus den angezeigten W&uuml;nschen k&ouml;nnt euch einen w&auml;hlen und ihn anklicken, dann bekommt ihr weitere Details angezeigt. Den Namen der/desjenigen, der den Wunsch eingetragen hat, erhaltet ihr erst wenn ihr euch entscheidet, den Wunsch zu erf&uuml;llen.<br>
Zusammen mit den Kontaktdaten erhaltet ihr auch eine <b>Geschenk-ID</b>, die ihr bitte unbedingt auf das Paket schreibt. Das ist sehr wichtig, denn sonst kann das Geschenk nicht best&auml;tigt werden wenn es angekommen ist!<br>
Nun habt ihr euch verpflichtet, diesen Wunsch zu erf&uuml;llen und er wird aus der Wunschliste gestrichen. Jetzt ist es Zeit sich mit der Umsetzung zu befassen.<br>
<br>
Es ist m&ouml;glich mehrere W&uuml;nsche zu erf&uuml;llen, allerdings kann erst dann einen weiteren Wunsch aussucht werden, wenn der erste als abgeschickt eingetragen wurde.<br>
